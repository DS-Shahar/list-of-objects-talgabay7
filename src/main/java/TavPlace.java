public class TavPlace {
	private String tav;
	private int place;
	public TavPlace(String tav, int place) {
		this.tav = tav;
		this.place = place;
	}
	public String getTav() {
		return tav;
	}
	public void setTav(String tav) {
		this.tav = tav;
	}
	public int getPlace() {
		return place;
	}
	public void setPlace(int place) {
		this.place = place;
	}
	
}
